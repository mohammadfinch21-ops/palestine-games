/** ترتيب المربعات على اللوحة (صفوف متعرجة: 1→10، 11→20، ...) */
export function getSerpentineIndex(square) {
  const row = Math.floor((square - 1) / 10);
  const col = (square - 1) % 10;
  const gridCol = row % 2 === 0 ? col : 9 - col;
  return row * 10 + gridCol;
}

